    private void btnThemSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSachActionPerformed
        String tenSach = txtTenSach.getText();
        String tenTheLoai = (String) cboTheLoai.getSelectedItem();
        String tenNhaXuatBan = (String) cboNhaXuatBan.getSelectedItem();
        String ngayNhap = txtNgayNhap.getText();
        float giaTien = 0;
        try {
            giaTien = Float.parseFloat(txtGiaTien.getText());

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(this, "Vui lòng nhập số.");
            txtGiaTien.setText("");
        }

        if (tenSach.equals("") || tenTheLoai.equals("")
                || tenNhaXuatBan.equals("") || ngayNhap.equals("")
                || giaTien <= 0 || txtGiaTien.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin sách!");
        } else {

            UserDAO userDAO = new UserDAO();
            try {
                userDAO.themSach(tenSach, tenTheLoai, tenNhaXuatBan, ngayNhap, giaTien);
            } catch (SQLException ex) {
                Logger.getLogger(DangKyJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

            hienThiBangSach();
        }
    }//GEN-LAST:event_btnThemSachActionPerformed

