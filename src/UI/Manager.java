    private void hienThiBangSach() {
        DefaultTableModel model = (DefaultTableModel) tblSach.getModel();
        model.setRowCount(0);
        for (Sach item : new UserDAO().timSach()) {
            model.addRow(new Object[]{
                item.getMaSach(),
                item.getTenSach(),
                item.getTenTheLoai(),
                item.getTenNhaXuatBan(),
                item.getNgayNhap(),
                item.getGiaTien()});
        }

    }

    private void hienThiBangTheLoai() {
        DefaultTableModel model = (DefaultTableModel) tblTheLoai.getModel();
        model.setRowCount(0);
        for (TheLoai item : new UserDAO().timTheLoai()) {
            model.addRow(new Object[]{
                item.getMaTheLoai(),
                item.getTenTheLoai()});
            cboTheLoai.addItem(item.getTenTheLoai());
        }

    }

    private void hienThiBangNhaXuatBan() {
        DefaultTableModel model = (DefaultTableModel) tblNhaXuatBan.getModel();
        model.setRowCount(0);
        for (NhaXuatBan item : new UserDAO().timNhaXuatBan()) {
            model.addRow(new Object[]{
                item.getMaNhaXuatBan(),
                item.getTenNhaXuatBan()});
            cboNhaXuatBan.addItem(item.getTenNhaXuatBan());
        }

    }

